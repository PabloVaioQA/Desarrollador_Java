public class Calculadora {
    
   public Calculadora() {
    }

    //METODOS CALCULADORA
    
    public double Suma(double unNumero, double otroNumero){
        
    return unNumero + otroNumero;
    }
    
    public double Resta(double unNumero, double otroNumero){
        
    return unNumero - otroNumero;
    }
    
    public double Multiplicacion(double unNumero, double otroNumero){
    return unNumero * otroNumero;
    }
    
    public double Division(double unNumero, double otroNumero){
    return unNumero /otroNumero;
    }
    
    
}
